

	import java.util.*;
	public class Area_of_rectangle
	{

		public static void main(String[] args) 
		{
			double l,b,A;
			Scanner sc=new Scanner(System.in);
			System.out.println("Enter value of l & b");
			l=sc.nextDouble();
			b=sc.nextDouble();
			A=l*b;
			System.out.println("Area of Rectangle="+A);		
		}
	}
